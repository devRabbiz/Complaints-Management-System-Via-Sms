<?php

class Status_code_m extends CI_Model {
	
//==============================================================
//**************************************************************
//==============================================================	
	function __construct()
    {
    	parent::__construct();
    }
//==============================================================
//**************************************************************
//==============================================================
    	function statusAdd($data)
	{
		$insert = $this->db->insert('tbl_status_code', $data);
		return $this->db->insert_id();
	}
//==============================================================
//**************************************************************
//==============================================================	
	function updateStatusById($data,$id)
	{
		$this->db->where('status_code_id',$id);
		$this->db->update('tbl_status_code',$data);
		return $this->db->affected_rows() > "";
	}
//==============================================================
//**************************************************************
//==============================================================	
	function  getStatusById($id){
		$this->db->select();
		$this->db->from('tbl_status_code');
		$this->db->where('status_code_id',$id);
		$query= $this->db->get();
		return $query->result_array();
	}
//==============================================================
//**************************************************************
//==============================================================	
	function  getStatusAll($per_pg="",$offset=""){

		$this->db->order_by('status_code_id','desc');
		$this->db->where('status_code_type','0');
		$query=$this->db->get('tbl_status_code',$per_pg,$offset);
		return $query->result_array();
	}
//==============================================================
//**************************************************************
//==============================================================	
	function  countAllStatus(){
		

		return $this->db->count_all('tbl_status_code');
	}
//==============================================================
//**************************************************************
//==============================================================	
	function  deleteStatusCodeById($id){
	$this->db->where('status_code_id', $id)->delete('tbl_status_code');
	return ($this->db->affected_rows() > 0) ? TRUE : FALSE;
	}
//==============================================================
//**************************************************************
//==============================================================	

}