<?php

class App_setting_m extends CI_Model {
//==============================================================
//**************************************************************
//==============================================================	
	function __construct()
    {
    	parent::__construct();
    }
//==============================================================
//**************************************************************
//==============================================================
    	function app_settingAdd($data)
	{
		$insert = $this->db->insert('tbl_app_setting', $data);
		return $this->db->insert_id();
	}
//==============================================================
//**************************************************************
//==============================================================	
	function updateSettingByKey($data,$key)
	{
		$this->db->where('app_setting_key',$key);
		$this->db->update('tbl_app_setting',$data);
		return $this->db->affected_rows() > "";
	}
//==============================================================
//**************************************************************
//==============================================================	
	function  getCustomerById($id){
		$this->db->select();
		$this->db->from('tbl_app_setting');
		$this->db->where('app_setting_id',$id);
		$query= $this->db->get();
		return $query->result_array();
	}
//==============================================================
//**************************************************************
//==============================================================
	function updateUserPasswordById($data,$id)
	{
		$this->db->where('user_id',$id);
		$this->db->update('tbl_user',$data);
		return $this->db->affected_rows() > "";
	}
//==============================================================
//**************************************************************
//==============================================================	
	function  getAppSettingAll(){
		$this->db->order_by('app_setting_id','desc');
		$query=$this->db->get('tbl_app_setting');	
		return $query->result_array();
	}
//==============================================================
//**************************************************************
//==============================================================	
	function  countAllCustomer(){

		return $this->db->count_all('tbl_app_setting');
	}
//==============================================================
//**************************************************************
//==============================================================	
	function  deleteCustomerById($id){
	$this->db->where('app_setting_id', $id)->delete('tbl_app_setting');
	return ($this->db->affected_rows() > 0) ? TRUE : FALSE;
	}
//==============================================================
//**************************************************************
//==============================================================	

}